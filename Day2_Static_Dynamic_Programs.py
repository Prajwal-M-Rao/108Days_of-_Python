# Static Programs- A program in which the values are fixed
# Eg: Company Websites i.e, same content visible to all users
a=int(10)
b=int(20)
c=a+b
print(c)

#Dynamic Programs- A program in which the values are user defined
#Eg: Youtube content/ Social Media platform content

e=int(input("Enter a value : "))
f=int(input("Enter another value : "))
g=e+f
print(g)